//=====================================| Import the Module |=====================================\\
const { Client } = require("discord.js");

//==========< SETTINGS >==========\\
const Config = require("../../Structures/Settings/config.json");

//==========< OTHERS >==========\\
const moment = require("moment");
const color = require("colors");

//=====================================| Code |=====================================\\

module.exports = {
    name: "disconnect",
    once: true,

    /**
     * @param {Client} client
     */

    async execute(client) {
        console.log(`${color.bold.bgBlue(`[${moment().format("dddd - DD/MM/YYYY - hh:mm:ss", true)}]`)} ` + `${color.bold.red(`[DISCONNECT]`)} ` + `${client.user.tag} `.magenta + `has been disconnected from Discord API...`.yellow);
        client.destroy();
    }
};

